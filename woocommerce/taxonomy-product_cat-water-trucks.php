                                <img src="<?php echo wc_placeholder_img_src('full'); ?>" alt="<?php echo esc_attr( get_the_title() ); ?>" class="img-fluid w-100">
                            <?php endif; ?>
                        </a>
                    </div>
                </div>

                <!-- Product Content -->
