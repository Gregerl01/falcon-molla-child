                            echo wpautop( $product->get_short_description() );
                        } else {
                            the_excerpt();
                        }
                        ?>
                    </div>

                    <?php
                    $attributes = $product->get_attributes();
                    if ( !empty( $attributes ) ) : ?>
                    <ul class="product-features list-unstyled mb-4">
                        <?php foreach ( $attributes as $attribute ) :
                            if ( $attribute->get_visible() ) :
                                $values = $attribute->is_taxonomy()
                                    ? wc_get_product_terms( $product->get_id(), $attribute->get_name(), ['fields' => 'names'] )
                                    : $attribute->get_options();
                        ?>
                        <li>
                            <i class="fas fa-check text-primary me-2"></i>
                            <strong><?php echo wc_attribute_label( $attribute->get_name() ); ?>:</strong>
                            <?php echo implode(', ', $values); ?>
                        </li>
                        <?php
                            endif;
                        endforeach; ?>
                    </ul>
                    <?php endif; ?>

                    <a href="<?php the_permalink(); ?>" class="btn btn-primary">Learn More</a>

                    </div>
                </div>

            </div>
